# 1) რიცხვის შემოწმება

num = int(input("შეიყვანე რიცხვი: "))

if num < 10:
    print(f"{num} არის 10-ზე ნაკლები")
elif num < 20:
    print(f"{num} არის 20-ზე ნაკლები")
else:
    print(f"{num} უფრო მეტი ან ტოლია 20-ისა")



# 2) ამინდის შემოწმება

weather = input("შეიყვანე ამინდი: ").lower()

if weather in ["მზიანი", "წვიმიანი"]:
    print("ვივარჯიშებ გარეთ")
else:
    print("არ ვივარჯიშებ დღეს")
